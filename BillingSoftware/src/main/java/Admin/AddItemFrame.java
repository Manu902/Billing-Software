    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        FileInputStream fis = null;
        int i = 0;
        String itemId = jTextField1.getText();
        String itemName = jTextField2.getText();
        String itemPrice = jTextField3.getText();
        String itemCategory = (String) jComboBox1.getSelectedItem();
        String itemQuantity = jTextField4.getText();
        String itemDesc = jTextArea1.getText();
        
        try
        {
            if(file != null)
                fis = new FileInputStream(file);
        }
        catch(FileNotFoundException e)
        {
            e.printStackTrace();
        }
        
        if(itemId.trim().equals(""))
            JOptionPane.showMessageDialog(rootPane, "Please enter the id number");
        else if(itemName.trim().equals(""))
            JOptionPane.showMessageDialog(rootPane, "Please enter the name of item");
        else if(itemPrice.trim().equals(""))
            JOptionPane.showMessageDialog(rootPane, "Please enter the price of item");
        else if(itemQuantity.trim().equals(""))
            JOptionPane.showMessageDialog(rootPane, "Please enter the quantity of item");
        else if(itemCategory.equals("") || itemCategory.equals("Select"))
            JOptionPane.showMessageDialog(rootPane, "Please select the category name");
        else
        {
            try
                (
                    Connection con = Database_C.getConnection();
                    PreparedStatement stm = con.prepareStatement("insert into store_Items values(?,?,?,?,?,?,?)");
                )
            {
                stm.setString(1, itemId);
                stm.setString(2, itemName);
                stm.setString(3, itemPrice);
                stm.setString(4, itemCategory);
                stm.setString(5, itemQuantity);
                stm.setString(6, itemDesc);
                if(fis != null)
                {
                    stm.setBinaryStream(7, fis, file.length());
                    i = stm.executeUpdate();
                }    
                else
                {
                   int result =  JOptionPane.showConfirmDialog(rootPane, "Do you add item without image" ,"Billing", JOptionPane.YES_NO_OPTION);
                   if(result == 0)
                   {
                       stm.setString(7, "");
                       i = stm.executeUpdate();
                   }   
                }  
                if(i>0)
                {
                    JOptionPane.showMessageDialog(rootPane, "Item added successfully");
                    createItemId();
                    jTextField2.setText("");
                    jTextField3.setText("");
                    jTextField4.setText("");
                    jTextArea1.setText("");
                    jComboBox1.setSelectedIndex(0);
                    
                    ImageIcon image = new ImageIcon(new ImageIcon("E:\\BillingSoftware\\src\\main\\java\\Images\\EmptyBox.png").getImage().getScaledInstance(jLabel10.getWidth(), jLabel10.getHeight(), Image.SCALE_DEFAULT));
                    jLabel10.setIcon(image);
                }
            }
            catch(SQLException ex)
            {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

