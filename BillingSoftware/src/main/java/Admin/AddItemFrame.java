    // method for add combobox items;
    void addComboBoxItem()
    {
        try
            (
                Connection con = Database_C.getConnection();
                PreparedStatement stm = con.prepareStatement("Select *from cat_Of_Item");
                ResultSet rs = stm.executeQuery();
            )
        {
            while(rs.next())
            {
                jComboBox1.addItem(rs.getString("Name"));
            }
        }
        catch(SQLException ex)
        {
            ex.printStackTrace();
        }
    }
    
     // method for create itemId
    void createItemId()
    {
       int itemId = 0;
       try
           (
               Connection con = Database_C.getConnection();
               PreparedStatement stm = con.prepareStatement("select count(*) as total from store_Items");
               ResultSet rs = stm.executeQuery();
           )
       {
           if(rs.next())
           {
               itemId = rs.getInt("total");
               System.out.println(itemId);
               jTextField1.setText("itm/bt/"+(itemId+1));
           }
          
       }
       catch(SQLException ex)
       {
           ex.printStackTrace();
       }
    }
    
