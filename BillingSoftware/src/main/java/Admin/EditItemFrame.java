    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        boolean f_Flag = false;
        FileInputStream fis = null;
        String itemId = jTextField1.getText();
        String itemName = jTextField2.getText();
        String itemPrice = jTextField3.getText();
        String itemCategory = (String) jComboBox1.getSelectedItem();
        String itemQuantity = jTextField4.getText();
        String itemDesc = jTextField5.getText();
        
         try
        {
            if(file != null)
                fis = new FileInputStream(file);
            else
                f_Flag = true;
        }
        catch(FileNotFoundException e)
        {
            e.printStackTrace();
        }
        
        try
            (
                Connection con = Database_C.getConnection();   
            )
        {
            PreparedStatement f_stm;
            if(f_Flag)
            {
                PreparedStatement stm1 = con.prepareStatement("update store_Items set item_Id =?, item_Name =?, item_Price =?, item_Category =? ,"
                        + "item_Quantity =?, item_Discription =? where item_Id = '"+itemId+"'");
                f_stm = stm1;
            }
            else
            {
                 PreparedStatement stm = con.prepareStatement("update store_Items set item_Id =?, item_Name =?, item_Price =?, item_Category =? ,"
                        + "item_Quantity =?, item_Discription =? , image =? where item_Id = '"+itemId+"'");
                f_stm = stm;
                f_stm.setBinaryStream(7, fis, file.length());
            }
            
            f_stm.setString(1, itemId);
            f_stm.setString(2, itemName);
            f_stm.setString(3, itemPrice);
            f_stm.setString(4, itemCategory);
            f_stm.setString(5, itemQuantity);
            f_stm.setString(6, itemDesc);
            
            int i = f_stm.executeUpdate();
            if(i>0)
                JOptionPane.showMessageDialog(rootPane, "Updated item successfully");
            else
                JOptionPane.showMessageDialog(rootPane, "Item not update due to some error", "Error", JOptionPane.ERROR_MESSAGE);
            f_stm.close();
        }
        catch(SQLException ex)
        {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

