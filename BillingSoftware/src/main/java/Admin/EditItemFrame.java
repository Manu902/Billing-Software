    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        
        ImageIcon image = new ImageIcon(new ImageIcon("E:\\BillingSoftware\\src\\main\\java\\Images\\EmptyBox.png").getImage().getScaledInstance(jLabel8.getWidth(), jLabel8.getHeight(), Image.SCALE_DEFAULT));
        jLabel8.setIcon(image);
        jButton1.setVisible(false);
        
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        String itemId, name, category;
        int sr_No = 0;
        
        try
            (
                Connection con = Database_C.getConnection();
                PreparedStatement stm = con.prepareStatement("select *from store_Items");
                ResultSet rs = stm.executeQuery();
            )
        {
            while(rs.next())
            {
                itemId = rs.getString("item_Id");
                name = rs.getString("item_Name");
                category = rs.getString("item_Category");
                sr_No+= 1;
                
                Object[] ob = {sr_No, itemId, name, category,};
                dtm.addRow(ob);
            }
        }
        catch(SQLException ex)
        {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_formWindowOpened

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        
        jButton1.setVisible(true);
        int row = jTable1.getSelectedRow();
        String itemId = (String) jTable1.getValueAt(row, 1);
        jTextField1.setEditable(false);
        
        if(flag == true)
        {
             try
            (
                Connection con = Database_C.getConnection();
                PreparedStatement stm = con.prepareStatement("Select *from cat_Of_Item");
                ResultSet rs = stm.executeQuery();
            )
            {
                while(rs.next())
                {
                    if(!rs.getString("Name").equals("Select"))
                        jComboBox1.addItem(rs.getString("Name"));
                }
            }
            catch(SQLException ex)
            {
                ex.printStackTrace();
            }
        }
       
        try
            (
                
                Connection con = Database_C.getConnection();
                PreparedStatement stm = con.prepareStatement("select *from store_Items where item_Id = '"+itemId+"'");
                ResultSet rs = stm.executeQuery();
            )
        {
            if(rs.next())
            {
                jTextField1.setText(rs.getString("item_Id"));
                jTextField2.setText(rs.getString("item_Name"));
                jTextField3.setText(rs.getString("item_Price"));
                jTextField4.setText(rs.getString("item_Quantity"));
                jTextField5.setText(rs.getString("item_Discription"));
                String s_C_Item = rs.getString("item_Category");
                jComboBox1.setSelectedItem(s_C_Item);
                
                InputStream inputStream = rs.getBinaryStream("image");
                BufferedImage im = ImageIO.read(inputStream);
                if(im != null)
                     jLabel8.setIcon(new ImageIcon(im.getScaledInstance(jLabel8.getWidth(), jLabel8.getHeight(), Image.SCALE_DEFAULT)));
                else
                {
                    ImageIcon image = new ImageIcon(new ImageIcon("E:\\BillingSoftware\\src\\main\\java\\Images\\EmptyBox.png").getImage().getScaledInstance(jLabel8.getWidth(), jLabel8.getHeight(), Image.SCALE_DEFAULT));
                    jLabel8.setIcon(image);
                }
            }
        }
        catch(SQLException |IOException ex)
        {
            ex.printStackTrace();
        }
        flag = false;
    }//GEN-LAST:event_jTable1MouseClicked

